public enum DPType {
    D("发送的部门", 0),
    P("发送的同事", 1);
    String name;
    int type;

    DPType(String name, int type) {
        this.name = name;
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
