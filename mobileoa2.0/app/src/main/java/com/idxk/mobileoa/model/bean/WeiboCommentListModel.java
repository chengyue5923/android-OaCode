package com.idxk.mobileoa.model.bean;

import java.util.List;

/**
 * Created by lenovo on 2015/3/7.
 */
public class WeiboCommentListModel extends BaseModel {
    private String comment_id;
    private String uid;
    private String content;
    private UserInfo user_info;
    private String ctime;
    private String row_id;
    private String to_uid;
    private String app_uid;
    private int has_attach = 0;

    private List<AttachBean> attach;

    public int getHas_attach() {
        return has_attach;
    }

    public void setHas_attach(int has_attach) {
        this.has_attach = has_attach;
    }

    public List<AttachBean> getAttachBeanList() {
        return attach;
    }

    public void setAttachBeanList(List<AttachBean> attachBeanList) {
        this.attach = attachBeanList;
    }

    public String getTo_uid() {
        return to_uid;
    }

    public void setTo_uid(String to_uid) {
        this.to_uid = to_uid;
    }

    public String getApp_uid() {
        return app_uid;
    }

    public void setApp_uid(String app_uid) {
        this.app_uid = app_uid;
    }

    public String getRow_id() {
        return row_id;
    }

    public void setRow_id(String row_id) {
        this.row_id = row_id;
    }

    public String getCtime() {
        return ctime;
    }

    public void setCtime(String ctime) {
        this.ctime = ctime;
    }

    public UserInfo getUser_info() {
        return user_info;
    }

    public void setUser_info(UserInfo user_info) {
        this.user_info = user_info;
    }

    public String getComment_id() {
        return comment_id;
    }

    public void setComment_id(String comment_id) {
        this.comment_id = comment_id;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    @Override
    public String toString() {
        return "WeiboCommentListModel{" +
                "comment_id='" + comment_id + '\'' +
                ", uid='" + uid + '\'' +
                ", content='" + content + '\'' +
                '}';
    }
}
