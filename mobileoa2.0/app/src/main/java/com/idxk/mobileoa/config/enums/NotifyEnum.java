public enum  NotifyEnum {
    login(1,"登陆"),
    sendShare(2,"发送"),
    replyWeibo(3,"评论"),
    upLoadUserPicture(4,"用户头像上传"),
    sendFeedBackContent(5, "发送"),
    baiDu(6, "绑定"),
    clearbaiDu(7, "接除绑定"),
    nomal(0,"");

    int typ;
    String toast;
    NotifyEnum(int code, String toast) {
        this.typ = code;
        this.toast = toast;
    }

    public int getTyp() {
        return typ;
    }

    public void setTyp(int typ) {
        this.typ = typ;
    }

    public String getToast() {
        return toast;
    }

    public void setToast(String toast) {
        this.toast = toast;
    }
}
